package BOJ_10951_AB4;

import java.util.Scanner;

public class BOJ_10951_AB4 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		while(sc.hasNextInt()) {
			
			int A = sc.nextInt();
			int B = sc.nextInt();
			
			System.out.println(A+B);
			
		}
		
		sc.close();
	}

}
